    public void letter(java.awt.event.KeyEvent evt){
    
        char letra = evt.getKeyChar();
        if (!Character.isLetter(letra)) {
            evt.consume();
        }
    }
    public void num(java.awt.event.KeyEvent evt){
    
        char letra = evt.getKeyChar();
        if (!Character.isDigit(letra)) {
            evt.consume();
        }
    }
